class Pojistenec(object):

    def __init__(self, jmeno, prijmeni, vek, cislo):
        self.jmeno = jmeno
        self.prijmeni = prijmeni
        self.cislo = cislo
        self.vek = vek

    def __str__(self):
        return self.jmeno + "   " + self.prijmeni + "   " + self.cislo + "   " + self.vek










